describe('readManifest', () => {
  // readManifest validates path is inside process.cwd()/.korinfra/ — use a unique subdir there
  let testSubdir: string;
  let tmpFile: string;

  beforeEach(() => {
    testSubdir = join(process.cwd(), '.korinfra', `test-${randomUUID()}`);
    mkdirSync(testSubdir, { recursive: true });
    tmpFile = join(testSubdir, 'manifest-test.json');
  });

  afterEach(() => {
    rmSync(testSubdir, { recursive: true, force: true });
  });

  it('reads valid JSON, empty object, nested structure', async () => {
    const data = { recommendations: ['EBS-001'], scan_id: 'scan-123', version: 2 };
    await writeFile(tmpFile, JSON.stringify(data), 'utf8');
    expect(await readManifest(tmpFile)).toEqual(data);

    await writeFile(tmpFile, '{}', 'utf8');
    expect(await readManifest(tmpFile)).toEqual({});

    const nested = { meta: { region: 'us-east-1', account: '123456789012' }, stats: { total: 5, savings: 490.50 } };
    await writeFile(tmpFile, JSON.stringify(nested), 'utf8');
    const result = await readManifest(tmpFile) as typeof nested;
    expect(result.meta.region).toBe('us-east-1');
    expect(result.stats.savings).toBeCloseTo(490.50);
  });

  it('rejects for nonexistent file and invalid JSON', async () => {
    await expect(readManifest(join(testSubdir, 'nonexistent.json'))).rejects.toThrow();

    await writeFile(tmpFile, 'not-valid-json', 'utf8');
    await expect(readManifest(tmpFile)).rejects.toThrow();
  });
});
