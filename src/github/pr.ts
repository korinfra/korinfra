// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export interface PRRecommendation {
  resource_id: string;
  title: string;
  description: string;
  current_config: string;
  recommended_config: string;
  estimated_savings: number;
  confidence: number;
  risk?: string;
  /** Security rule ID (e.g. "SG-SEC-001"). Used in the security PR findings table. */
  ruleId?: string;
  /** Finding severity for security PRs ("critical" | "high" | "medium" | "low"). */
  severity?: string;
}

export interface PROptions {
  title?: string;
  body?: string;
  head: string;
  base?: string;
  draft?: boolean;
  labels?: string[];
  reviewers?: string[];
}

export interface PR {
  number: number;
  html_url: string;
  title: string;
  state: string;
  draft: boolean;
  created_at: string;
}

/** Minimal structure for korinfra-manifest.json */
export type korinfraManifest = Record<string, unknown>;

