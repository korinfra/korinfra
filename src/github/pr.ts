/**
 * Builds the standard korinfra PR body from recommendation data.
 * Mirrors Go BuildPRBody in internal/github/pr.go.
 */
export function buildPRBody(
  recommendations: PRRecommendation[],
  totalSavings: number,
  currency?: string,
): string {
  const sym = (!currency || currency === 'USD') ? '$' : `${currency} `;
  const isSecurity = recommendations.some(r => r.ruleId ?? r.severity) ||
    (totalSavings === 0 && recommendations.length > 0 && recommendations.every(r => r.estimated_savings === 0));
  const lines: string[] = [];

  const heading = isSecurity ? '## korinfra Security Fix' : '## korinfra Cost Optimization';
  lines.push(`${heading}\n`);
  lines.push(
    `> Generated by [korinfra](${REPO_URL}) — Smart infrastructure. Lower costs. One command at a time.\n`,
  );

  const esc = (s: string) => s
    .replace(/\\/g, '\\\\')
    .replace(/[\r\n]+/g, ' ')
    .replace(/\|/g, '\\|')
    .replace(/`/g, "'")
    .replace(/[[\]!*_~]/g, (ch) => `\\${ch}`);

  if (isSecurity) {
    const SEVERITY_ORDER = ['critical', 'high', 'medium', 'low'];
    const resourceNames = [...new Set(recommendations.map(r => `\`${esc(r.resource_id)}\``))].join(', ');
    lines.push(`This PR applies ${recommendations.length} security ${recommendations.length === 1 ? 'fix' : 'fixes'} to ${resourceNames}, detected by korinfra static analysis.\n`);

    // Group by severity
    const bySeverity = new Map<string, PRRecommendation[]>();
    for (const rec of recommendations) {
      const sev = rec.severity ?? 'medium';
      const bucket = bySeverity.get(sev) ?? [];
      bucket.push(rec);
      bySeverity.set(sev, bucket);
    }

    const SEV_EMOJI: Record<string, string> = { critical: '🔴', high: '🟠', medium: '🟡', low: '🟢' };
    lines.push('### Security Findings Fixed\n');
    for (const sev of SEVERITY_ORDER) {
      const group = bySeverity.get(sev);
      if (!group || group.length === 0) continue;
      const emoji = SEV_EMOJI[sev] ?? '';
      lines.push(`#### ${emoji} ${sev.charAt(0).toUpperCase() + sev.slice(1)} (${group.length})\n`);
      lines.push('| Rule | Resource | Issue | Fix Applied |');
      lines.push('|------|----------|-------|-------------|');
      for (const rec of group) {
        const ruleId = esc(rec.ruleId ?? rec.title.split(' ')[0] ?? '—');
        lines.push(`| \`${ruleId}\` | \`${esc(rec.resource_id)}\` | ${esc(rec.description)} | ${esc(rec.recommended_config)} |`);
      }
      lines.push('');
    }

    lines.push('### What Changed\n');
    recommendations.forEach((rec, i) => {
      const confidence = (rec.confidence * 100).toFixed(0);
      const ruleTag = rec.ruleId ? ` \`${esc(rec.ruleId)}\`` : '';
      lines.push(`**${i + 1}. ${esc(rec.title)}**${ruleTag}`);
      lines.push(`- **Resource:** \`${esc(rec.resource_id)}\``);
      lines.push(`- **Why it matters:** ${esc(rec.description)}`);
      if (rec.current_config) {
        lines.push(`- **Before:** \`${esc(rec.current_config)}\``);
      }
      lines.push(`- **After:** \`${esc(rec.recommended_config)}\``);
      lines.push(`- **Confidence:** ${confidence}%${rec.risk ? ` · **Risk:** ${esc(rec.risk)}` : ''}`);
      lines.push('');
    });
  } else {
    lines.push('### Cost Impact\n');
    lines.push(
      '| Resource | Current Config | Recommended | Est. Savings/mo | Confidence |',
    );
    lines.push(
      '|----------|---------------|-------------|-----------------|------------|',
    );
    for (const rec of recommendations) {
      const savings = rec.estimated_savings.toFixed(2);
      const confidence = (rec.confidence * 100).toFixed(0);
      const safeResourceId = esc(rec.resource_id).replace(/`/g, "'");
      lines.push(
        `| \`${safeResourceId}\` | ${esc(rec.current_config)} | ${esc(rec.recommended_config)} | ${sym}${savings} | ${confidence}% |`,
      );
    }
    lines.push('');
    lines.push(`**Total estimated savings: ${sym}${totalSavings.toFixed(2)}/month**\n`);

    lines.push('### Recommendations\n');
    recommendations.forEach((rec, i) => {
      lines.push(`${i + 1}. **${esc(rec.title)}** — ${esc(rec.description)}`);
      if (rec.risk) {
        lines.push(`   - Risk: ${esc(rec.risk)}`);
      }
    });
  }

  lines.push('');
  lines.push('### How to Review\n');
  if (isSecurity) {
    lines.push('- [ ] Review Terraform changes — confirm fix is correct and minimal');
    lines.push('- [ ] Run `terraform plan` to preview AWS changes');
    lines.push('- [ ] Test in a non-production environment first');
    lines.push('- [ ] Apply with `terraform apply` after approval');
    lines.push('- [ ] Re-run `korinfra security` to confirm finding is resolved\n');
  } else {
    lines.push('- [ ] Review each changed file — confirm recommendation is appropriate');
    lines.push('- [ ] Test in a non-production environment first');
    lines.push('- [ ] Run `terraform plan` then `terraform apply`\n');
  }

  const now = new Date();
  const ts = now.toISOString().replace('T', ' ').slice(0, 16) + ' UTC';
  lines.push(`---\n*Generated by [korinfra](${REPO_URL}) at ${ts} · [Docs](${REPO_URL}#readme)*`);

  return lines.join('\n');
}

