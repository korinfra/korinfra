/**
 * Creates a pull request and optionally adds labels and reviewers.
 */
export async function createPR(
  client: GitHubClient,
  owner: string,
  repo: string,
  opts: PROptions,
): Promise<PR> {
  const base = opts.base ?? 'main';
  const title = opts.title ?? 'korinfra: Cost Optimization Changes';

  const payload = {
    title,
    body: opts.body ?? '',
    head: opts.head,
    base,
    draft: opts.draft ?? false,
  };

  const pr = await client.post<PR>(
    `/repos/${owner}/${repo}/pulls`,
    payload,
  );

  if (!pr) {
    throw new Error('GitHub returned empty response when creating PR');
  }

  if (typeof pr.number !== 'number') {
    throw new Error('Unexpected GitHub API response shape: missing pr.number');
  }

  // Labels — best-effort, non-fatal
  if (opts.labels && opts.labels.length > 0) {
    try {
      await addLabels(client, owner, repo, pr.number, opts.labels);
    } catch (err: unknown) {
      logger.warn(
        { err: { message: redact(err instanceof Error ? err.message : String(err), 'moderate'), status: (err as { status?: number }).status } },
        'Failed to add labels to PR (non-fatal)'
      );
    }
  }

  // Reviewers — best-effort, non-fatal
  if (opts.reviewers && opts.reviewers.length > 0) {
    try {
      await requestReviewers(client, owner, repo, pr.number, opts.reviewers);
    } catch (err: unknown) {
      logger.warn(
        { err: { message: redact(err instanceof Error ? err.message : String(err), 'moderate'), status: (err as { status?: number }).status } },
        'Failed to add reviewers to PR (non-fatal)'
      );
    }
  }

  return pr;
}

/**
 * Adds labels to a PR, creating them on the repo first if missing.
 */
export async function addLabels(
  client: GitHubClient,
  owner: string,
  repo: string,
  prNumber: number,
  labels: string[],
): Promise<void> {
  // Ensure labels exist — ignore errors (422 = already exists)
  await Promise.allSettled(
    labels.map((label) =>
      client.post(`/repos/${owner}/${repo}/labels`, {
        name: label,
        color: labelColor(label),
      }),
    ),
  );

  await client.post(`/repos/${owner}/${repo}/issues/${prNumber}/labels`, {
    labels,
  });
}

/**
 * Requests reviewers on an open pull request.
 */
export async function requestReviewers(
  client: GitHubClient,
  owner: string,
  repo: string,
  prNumber: number,
  reviewers: string[],
): Promise<void> {
  await client.post(
    `/repos/${owner}/${repo}/pulls/${prNumber}/requested_reviewers`,
    { reviewers },
  );
}

// ---------------------------------------------------------------------------
// Manifest helpers (used by `fix pr` subcommand)
// ---------------------------------------------------------------------------

async function validateManifestPath(filePath: string): Promise<void> {
  let realPath: string;
  try {
    realPath = await realpath(filePath);
  } catch {
    // File doesn't exist yet or is unreachable; validate the resolved path
    realPath = resolve(filePath);
  }
  const allowedDir = join(process.cwd(), '.korinfra');
  const normalizedAllowed = allowedDir.endsWith(sep) ? allowedDir : allowedDir + sep;
  if (!realPath.startsWith(normalizedAllowed) && realPath !== allowedDir) {
    throw new Error('Manifest path must be inside .korinfra/ directory');
  }
}

/**
 * Reads korinfra-manifest.json from disk.
 */
export async function readManifest(path: string): Promise<korinfraManifest> {
  await validateManifestPath(path);
  const raw = await readFile(path, 'utf8');
  try {
    return JSON.parse(raw) as korinfraManifest;
  } catch (err) {
    throw new Error(`Failed to parse manifest at ${path}: ${err instanceof Error ? err.message : String(err)}`, { cause: err });
  }
}

/**
 * Writes korinfra-manifest.json to disk.
 */
export async function writeManifest(
  path: string,
  data: korinfraManifest,
): Promise<void> {
  await validateManifestPath(path);
  const writeOpts = process.platform !== 'win32'
    ? { encoding: 'utf8' as const, mode: 0o600 }
    : 'utf8';
  await writeFile(path, JSON.stringify(data, null, 2) + '\n', writeOpts);
}
