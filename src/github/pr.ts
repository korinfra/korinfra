const LABEL_COLORS: Record<string, string> = {
  korinfra: '0075ca',
  'cost-optimization': 'e4e669',
  terraform: '7057ff',
};

function labelColor(label: string): string {
  return LABEL_COLORS[label] ?? 'ededed';
}

